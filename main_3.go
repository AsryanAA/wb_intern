/*
	Дана последовательность чисел: 2,4,6,8,10. Найти сумму их квадратов
	(22+32+42….) с использованием конкурентных вычислений.
*/

package main

import (
	"fmt"
	"sync"
)

func main() {
	// Исходная последовательность чисел
	numbers := []int{2, 4, 6, 8, 10}

	// Создаем канал для передачи результатов
	resultChan := make(chan int)

	// Создаем WaitGroup для ожидания завершения всех горутин
	var wg sync.WaitGroup

	// Запускаем горутину для каждого числа
	for _, num := range numbers {
		wg.Add(1)
		go func(x int) {
			defer wg.Done()
			resultChan <- x * x // Вычисляем квадрат числа и отправляем в канал
		}(num)
	}

	// Запускаем горутину для суммирования результатов
	go func() {
		wg.Wait()
		close(resultChan) // Закрываем канал после завершения всех горутин
	}()

	// Суммируем результаты из канала
	sum := 0
	for result := range resultChan {
		sum += result
	}

	// Выводим сумму квадратов
	fmt.Println("Сумма квадратов чисел:", sum)
}
